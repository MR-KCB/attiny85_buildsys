/**
 * @brief Run as principal main instrucction 
 * 
 */
int main(void) {
    /* All init routines here */
    startup_init(); // specific mcu instructtions (e.g. clock source)
    SetDirection(kPortB, kOutput, kBit1, kLow);
    SetOutputValue(kPortB, kBit5, kHigh);

    for (;;) {
        SetOutputValue(kPortB, kBit1, kHigh);
        _delay_ms(500);
        SetOutputValue(kPortB, kBit1, kLow);
        _delay_ms(500);
    }
    return 0;
}                                                                         
                                                                               
